import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const packageDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = join(packageDir, "..", "..");
const apiPath = join(repoRoot, "crates", "tellegen", "src", "api.rs");
const outputPath = join(packageDir, "src", "generated", "contracts.ts");
const check = process.argv.includes("--check");

const packageJson = JSON.parse(
  readFileSync(join(packageDir, "package.json"), "utf8"),
);
const source = readFileSync(apiPath, "utf8");
const hash = createHash("sha256").update(source).digest("hex");

function rustEnumVariants(name) {
  const match = source.match(
    new RegExp(`pub enum ${name} \\{([\\s\\S]*?)\\n\\}`),
  );
  if (!match) throw new Error(`Could not find Rust enum ${name}`);
  return [...match[1].matchAll(/^    ([A-Z][A-Za-z0-9]*),/gm)].map((m) => m[1]);
}

function lowerTag(name) {
  return name.toLowerCase();
}

const formulationIds = rustEnumVariants("Problem").map(lowerTag);
const solveStatuses = rustEnumVariants("SolveStatus").map(lowerTag);

const generated = `// Generated by packages/engine/scripts/generate-contracts.mjs.
// Rust source: crates/tellegen/src/api.rs
// Rust source sha256: ${hash}

export const CONTRACT_VERSION = ${JSON.stringify(packageJson.version)} as const;
export const CONTRACT_SOURCE_SHA256 = ${JSON.stringify(hash)} as const;

export const FORMULATION_IDS = ${JSON.stringify(formulationIds)} as const;
export type FormulationId = (typeof FORMULATION_IDS)[number];

export const SOLVE_STATUSES = ${JSON.stringify(solveStatuses)} as const;
export type SolveStatus = (typeof SOLVE_STATUSES)[number];

export type BrowserFormulation = Extract<FormulationId, 'dcopf' | 'acopf' | 'socwr'>;
export type Power = 'Active' | 'Reactive';
export type End = 'From' | 'To';
export type VoltageKind = 'Magnitude' | 'Angle' | 'Squared' | 'ProductReal' | 'ProductImag';
export type CostTerm = 'Quadratic' | 'Linear';
export type GB = 'Conductance' | 'Susceptance';
export type Bound = 'Min' | 'Max';
export type TapKind = 'Ratio' | 'PhaseShift';
export type Mode = 'Forward' | 'Adjoint' | 'Auto';

export type Operand =
\t| { Price: Power }
\t| { Dispatch: Power }
\t| { Flow: { power: Power; end: End } }
\t| { Voltage: VoltageKind };

export type Parameter =
\t| { Demand: Power }
\t| { Cost: CostTerm }
\t| 'LineLimit'
\t| { SeriesAdmittance: GB }
\t| { ShuntAdmittance: GB }
\t| { VoltageBound: Bound }
\t| { GenBound: { power: Power; bound: Bound } }
\t| { Transformer: TapKind }
\t| 'Switching';

export type ElementId = { Bus: number } | { Branch: number } | { Generator: number };

export interface SensRequest {
\toperand: Operand;
\tparameter: Parameter;
\tindices?: number[] | null;
\tmode?: Mode;
}

export interface Edits {
\tdeltas?: DemandDeltas;
\trates?: BranchRatingDeltas;
}

export interface SolveOptions {
\tshed?: boolean;
\twarm_start?: boolean;
}

export interface SolveRequest {
\tformulation?: FormulationId;
\tedits?: Edits;
\tsensitivities?: SensRequest[];
\toptions?: SolveOptions;
}

export interface SolveIteration {
\titer: number;
\tobjective: number;
\tinf_pr: number;
\tinf_du: number;
}

export type NewtonIterations = { count: number; residual: number };
export type Iterations = SolveIteration[] | NewtonIterations;

export interface BusScalar {
\tbus: number;
\tvalue: number;
}

export interface BusInjection {
\tbus: number;
\tp: number;
\tq: number;
}

export interface BranchFlow {
\tbranch: number;
\tpf: number;
\tloading: number;
\tqf?: number;
\tpt?: number;
\tqt?: number;
}

export interface GenDispatch {
\tgen: number;
\tpg: number;
\tqg?: number;
}

export interface RowMeta {
\toperand: Operand;
\telement: ElementId;
\tindex: number;
}

export interface ColMeta {
\tparameter: Parameter;
\telement: ElementId;
\tindex: number;
}

export interface SensitivityMatrix {
\tvalues: number[][];
\trows: RowMeta[];
\tcols: ColMeta[];
\tunits: string;
}

export interface SolveResponse {
\tformulation: FormulationId;
\tstatus: SolveStatus;
\tobjective?: number;
\titerations?: Iterations;
\tlmp?: BusScalar[];
\tlmp_q?: BusScalar[];
\tvm?: BusScalar[];
\tva?: BusScalar[];
\tw?: BusScalar[];
\tinjections?: BusInjection[];
\tflows?: BranchFlow[];
\tdispatch?: GenDispatch[];
\tsensitivities?: SensitivityMatrix[];
}

export interface ProblemCaps {
\tformulation: FormulationId;
\tavailable: boolean;
\tblocks: string[];
\toperands?: Operand[];
\tparameters?: Parameter[];
}

export interface NetworkBus {
\tid: number;
\tlon: number;
\tlat: number;
\tdemand_mw: number;
\tgen_mw: number;
}

export interface NetworkBranch {
\tid: number;
\tfrom: number;
\tto: number;
\trate_mw: number;
\tstatus: number;
\tpath: [number, number][];
}

export interface Network {
\tid: string;
\tname: string;
\tbase_mva: number;
\tsynthetic_coords: boolean;
\tbuses: NetworkBus[];
\tbranches: NetworkBranch[];
}

export interface Solution {
\tobjective: number;
\tlmp: { bus: number; usd_per_mwh: number }[];
\tva: { bus: number; value: number }[];
\tw: { bus: number; value: number }[];
\tflows: { branch: number; mw: number; loading: number }[];
\tdispatch: { gen: number; mw: number }[];
}

export interface SensitivityColumn {
\tcase: string;
\toperand: string;
\tparameter: string;
\t/** The selected source bus, for a bus-axis parameter column (∂LMP/∂d). */
\tbus?: number;
\t/** The selected source branch, for a branch-axis parameter column (∂LMP/∂fmax). */
\tbranch?: number;
\tunits: string;
\tvalues: { bus: number; value: number }[];
}

export interface CaseSummary {
\tid: string;
\tname: string;
\tn_bus: number;
\tn_branch: number;
\tn_gen: number;
}

export type DemandDeltas = Record<number, number>;

export type BranchRatingDeltas = Record<number, number>;
`;

if (check) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== generated) {
    console.error(
      "Generated engine contracts are stale. Run `npm run contracts` from the repository root.",
    );
    process.exit(1);
  }
} else {
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, generated);
}
